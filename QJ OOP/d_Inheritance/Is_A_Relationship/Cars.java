public class Cars {
    public int carWheels;
    public int carSeats;
    public int carDoors;
    public int carWe(int carWheels){
        this.carWheels = carWheels;
        return this.carWheels;
    }
    public int carSe(int carSeats){
        this.carSeats = carSeats;
        return this.carSeats;
    }
    public int carDo(int carDoors){
        this.carDoors = carDoors;
        return this.carDoors;
    }
    
}
